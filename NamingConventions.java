public class NamingConventions {
	/*  
	 * Class names are Nouns. First letter of class name should be in Capital.
	 * 		e.g. - Student, Person, Computer
	 * 
	 * Interface names are adjective. First letter of interface name should be in Capital.
	 * 		e.g. - Runnable, Readable, Remote.
	 * 
	 * Method names are usually verbs. First letter of methods should be in lower case.
	 * 		e.g. - print, actionPerformed, write.
	 * 
	 * Variable names are also written with first letter in lower case.
	 * 		e.g. - stockprice, bankdetails.
	 * 
	 * package names also start with small case. package name does not have camel case. all letters are small.
	 * 
	 * Constructor is a class and its name follows rules of class name. Its first letter is capital.
	 * 
	 * Constants are written in all capital letters.
	 * 		e.g. - PI, DENSITY, MAX_PRICE.
	 * 
	 * Common Rule. Java follows camel case. That is when you combine two words you capitalize the first letter of second word.
	 */
	
}
